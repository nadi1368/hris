    public function search($params)
    {
        $query = ContractTemplates::find();

        // add conditions that should always apply here

        $dataProvider = new ActiveDataProvider([
            'query' => $query,
        ]);

        $this->load($params);

        if (!$this->validate()) {
            // comment the following line if you do want to return any records when validation fails
            $query->where('0=1');
            return $dataProvider;
        }

